// 백엔드가 한글 데이터를 저장할 때 UTF-8 바이트를 CP1252로 잘못 읽어들여서
// 과목명·전공명(sectionGroup) 등이 깨진 채로 내려온다.
// CP1252로 다시 인코딩해 원래 UTF-8 바이트로 되돌린 뒤 UTF-8로 디코딩하면 복원된다.
// 백엔드 인코딩 버그가 고쳐지면 이 파일은 통째로 지워도 된다.
const CP1252_SPECIAL = {
  0x20ac: 0x80, 0x201a: 0x82, 0x0192: 0x83, 0x201e: 0x84, 0x2026: 0x85,
  0x2020: 0x86, 0x2021: 0x87, 0x02c6: 0x88, 0x2030: 0x89, 0x0160: 0x8a,
  0x2039: 0x8b, 0x0152: 0x8c, 0x017d: 0x8e, 0x2018: 0x91, 0x2019: 0x92,
  0x201c: 0x93, 0x201d: 0x94, 0x2022: 0x95, 0x2013: 0x96, 0x2014: 0x97,
  0x02dc: 0x98, 0x2122: 0x99, 0x0161: 0x9a, 0x203a: 0x9b, 0x0153: 0x9c,
  0x017e: 0x9e, 0x0178: 0x9f,
}

export function fixMojibake(s) {
  if (!s) return s
  const bytes = []
  for (const ch of s) {
    const codePoint = ch.codePointAt(0)
    const byte = CP1252_SPECIAL[codePoint] ?? codePoint
    if (byte > 0xff) return s // CP1252 바이트 범위를 벗어나면 애초에 깨진 텍스트가 아니었던 것
    bytes.push(byte)
  }
  try {
    return new TextDecoder('utf-8', { fatal: true }).decode(new Uint8Array(bytes))
  } catch {
    return s // 복원 실패 시 원본 그대로 보여준다
  }
}
